@Configuration
@EnableSwagger2
public class Swagger2 {

    @Bean
    public Docket createRestApi() {
        return new Docket(DocumentationType.SWAGGER_2)
                .apiInfo(apiInfo())
                .select()
                .apis(RequestHandlerSelectors.basePackage("com.nasus.web"))
                .paths(PathSelectors.any())
                .build();
    }

    private ApiInfo apiInfo() {
        return new ApiInfoBuilder()
                .title("Spring Boot中使用Swagger2构建RESTful APIs")
                .description("更多Spring Boot相关文章请关注：https://blog.csdn.net/turodog")
                .termsOfServiceUrl("https://blog.csdn.net/turodog")
                .contact(new Contact("一个优秀的废人","https://blog.csdn.net/turodog","turoDog@foxmail.com"))
                .version("1.0")
                .build();
    }

}
